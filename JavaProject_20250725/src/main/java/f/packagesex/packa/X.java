package f.packagesex.packa;

public class X {

	public int pu=5;
	protected int ch=10;
	int d = 50;
	private int pr=100;
	
	public static void main(String[] args) {
		X obj = new X();
		
		System.out.println(obj.pu);
		System.out.println(obj.ch);
		System.out.println(obj.d);
		System.out.println(obj.pr);
	}

}
